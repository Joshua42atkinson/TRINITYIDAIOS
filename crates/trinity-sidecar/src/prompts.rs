/// Build the planning prompt for Opus given a quest
pub fn opus_plan_prompt(
    quest_title: &str,
    quest_description: &str,
    context_files: &[(String, String)],
) -> String {
    let mut prompt = format!("## Quest: {}\n\n{}\n\n", quest_title, quest_description);

    if !context_files.is_empty() {
        prompt.push_str("## Context Files:\n\n");
        for (path, content) in context_files {
            prompt.push_str(&format!("### {}\n```\n{}\n```\n\n", path, content));
        }
    }

    prompt.push_str("Create a detailed execution plan for this quest. Output valid JSON.");
    prompt
}

/// Build the planning prompt with MCP semantic context
/// This enables context-aware planning when MCP has indexed additional relevant files
pub fn opus_plan_prompt_with_mcp(
    quest_title: &str,
    quest_description: &str,
    context_files: &[(String, String)],
    mcp_context_chunks: &[String],
) -> String {
    let mut prompt = format!("## Quest: {}\n\n{}\n\n", quest_title, quest_description);

    if !context_files.is_empty() {
        prompt.push_str("## Required Context Files:\n\n");
        for (path, content) in context_files {
            prompt.push_str(&format!("### {}\n```\n{}\n```\n\n", path, content));
        }
    }

    if !mcp_context_chunks.is_empty() {
        prompt.push_str("## Additional Relevant Context (from MCP semantic search):\n\n");
        for (i, chunk) in mcp_context_chunks.iter().enumerate() {
            prompt.push_str(&format!("### Chunk {}\n{}\n\n", i + 1, chunk));
        }
    }

    prompt.push_str("Create a detailed execution plan for this quest. Output valid JSON.");
    prompt
}

/// Build the code generation prompt for REAP given a step spec
/// Uses PATCH MODE for existing files >100 lines to avoid truncation
pub fn reap_code_prompt(
    step_spec: &str,
    target_file: &str,
    existing_content: Option<&str>,
) -> String {
    let mut prompt = format!(
        "## Task: Generate code for `{}`\n\n{}\n\n",
        target_file, step_spec
    );

    if let Some(existing) = existing_content {
        let line_count = existing.lines().count();
        if line_count > 100 {
            // PATCH MODE for large files — only output the changed section
            prompt.push_str(&format!(
                "## Existing file ({} lines) — DO NOT REWRITE THE ENTIRE FILE\n\n\
                 ```rust\n{}\n```\n\n\
                 ## IMPORTANT: PATCH MODE\n\
                 This file is {} lines long. Do NOT output the entire file.\n\
                 Instead, output ONLY a patch in this exact format:\n\n\
                 ```patch\n\
                 --- a/{target}\n\
                 +++ b/{target}\n\
                 @@ -LINE_START,COUNT +LINE_START,COUNT @@\n\
                  context line (unchanged, starts with space)\n\
                 -removed line (starts with minus)\n\
                 +added line (starts with plus)\n\
                  context line (unchanged)\n\
                 ```\n\n\
                 Include 3 lines of context before and after each change.\n\
                 If you need multiple changes, use multiple @@ hunks.\n\
                 Be MINIMAL — change only what's needed to fix the issue.\n",
                line_count,
                existing,
                line_count,
                target = target_file
            ));
        } else {
            // Small files: output the complete updated file
            prompt.push_str(&format!(
                "## Existing file content ({} lines):\n```rust\n{}\n```\n\n\
                 Output the COMPLETE updated file with your changes applied.\n",
                line_count, existing
            ));
        }
    } else {
        prompt.push_str("This is a NEW file. Generate the complete content.\n");
    }

    prompt
}

