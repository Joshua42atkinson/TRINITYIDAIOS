// Trinity Voice Performance Benchmark
// Copyright (c) Joshua
// Shared under license for Ask_Pete (Purdue University)

use criterion::{black_box, criterion_group, criterion_main, Criterion};
use trinity_body::voice_integration_manager::*;
use trinity_body::trinity_face::*;

fn bench_voice_processing(c: &mut Criterion) {
    let rt = tokio::runtime::Runtime::new().unwrap();
    
    c.bench_function("voice_processing_100ms", |b| {
        b.iter(|| {
            rt.block_on(async {
                let mut manager = VoiceIntegrationManager::new().await.unwrap();
                let test_audio = vec![0.1f32; 1600]; // 100ms at 16kHz
                
                let result = manager.process_audio_input(
                    black_box(test_audio), 
                    black_box(TrinityPersona::Sage)
                ).await.unwrap();
                
                black_box(result)
            })
        })
    });
    
    c.bench_function("educational_context_extraction", |b| {
        let manager = rt.block_on(VoiceIntegrationManager::new()).unwrap();
        
        b.iter(|| {
            let transcription = "Educational content about machine learning fundamentals and instructional design principles";
            let context = manager.extract_educational_context(black_box(transcription));
            black_box(context)
        })
    });
}

criterion_group!(benches, bench_voice_processing);
criterion_main!(benches);
EOF

