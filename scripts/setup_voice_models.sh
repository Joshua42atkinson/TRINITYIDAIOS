# Create model directories
echo "📁 Creating model directories..."
mkdir -p assets/models/voice
mkdir -p models/voice

# Check if models already exist
if [ -f "assets/models/voice/whisper-tiny.onnx" ]; then
    echo "✅ Whisper Tiny model already exists"
    WHISPER_EXISTS=true
else
    echo "⬇️  Downloading Whisper Tiny model..."
    WHISPER_EXISTS=false
fi

# Download Whisper Tiny ONNX model (39MB)
if [ "$WHISPER_EXISTS" = false ]; then
    echo "📥 Downloading Whisper Tiny (39MB)..."
    
    # Try multiple sources for Whisper Tiny ONNX
    WHISPER_URLS=(
        "https://huggingface.co/openai/whisper-tiny/resolve/main/model.onnx"
        "https://github.com/openai/whisper/raw/main/models/tiny.pt"
        "https://huggingface.co/SnyderLL/whisper-tiny-onnx/resolve/main/whisper-tiny.onnx"
    )
    
    DOWNLOADED=false
    for url in "${WHISPER_URLS[@]}"; do
        echo "  Trying: $url"
        if wget -O assets/models/voice/whisper-tiny.onnx --timeout=30 --tries=2 "$url" 2>/dev/null; then
            echo "  ✅ Downloaded successfully"
            DOWNLOADED=true
            break
        else
            echo "  ❌ Failed, trying next source..."
        fi
    done
    
    if [ "$DOWNLOADED" = false ]; then
        echo "⚠️  Could not download Whisper model from any source"
        echo "   Creating placeholder for development..."
        echo "PLACEHOLDER_WHISPER_MODEL" > assets/models/voice/whisper-tiny.onnx
    fi
fi

# Create Mimi codec models (placeholders for now)
echo "🎵 Setting up Mimi codec models..."

if [ ! -f "assets/models/voice/mimi_encoder.onnx" ]; then
    echo "  Creating Mimi encoder placeholder..."
    echo "PLACEHOLDER_MIMI_ENCODER_MODEL" > assets/models/voice/mimi_encoder.onnx
fi

if [ ! -f "assets/models/voice/mimi_decoder.onnx" ]; then
    echo "  Creating Mimi decoder placeholder..."
    echo "PLACEHOLDER_MIMI_DECODER_MODEL" > assets/models/voice/mimi_decoder.onnx
fi

# Verify model files
echo "🔍 Verifying model files..."
WHISPER_SIZE=$(stat -f%z assets/models/voice/whisper-tiny.onnx 2>/dev/null || stat -c%s assets/models/voice/whisper-tiny.onnx 2>/dev/null || echo "0")

if [ "$WHISPER_SIZE" -gt 1000000 ]; then  # > 1MB indicates real model
    echo "✅ Whisper Tiny model: $(echo $WHISPER_SIZE | numfmt --to=iec) - REAL MODEL"
else
    echo "⚠️  Whisper Tiny model: placeholder - will use mock processing"
fi

# Create model configuration
echo "⚙️  Creating model configuration..."
cat > assets/models/voice/model_config.yaml << 'EOF'
