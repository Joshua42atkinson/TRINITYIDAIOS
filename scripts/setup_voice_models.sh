python3 << 'PYTHON' 2>/dev/null || echo "⚠️  Python not available, skipping test audio generation"
import numpy as np
import wave
import struct

# Generate 1 second of 440Hz sine wave at 16kHz
sample_rate = 16000
duration = 1.0
frequency = 440.0

t = np.linspace(0, duration, int(sample_rate * duration), False)
audio_data = np.sin(2 * np.pi * frequency * t) * 0.5  # 50% volume

# Convert to 16-bit PCM
audio_int16 = (audio_data * 32767).astype(np.int16)

# Write WAV file
with wave.open('assets/test/audio/test_tone_440hz.wav', 'w') as wav_file:
    wav_file.setnchannels(1)  # Mono
    wav_file.setsampwidth(2)  # 16-bit
    wav_file.setframerate(sample_rate)
    wav_file.writeframes(audio_int16.tobytes())

print("✅ Test audio generated: assets/test/audio/test_tone_440hz.wav")
PYTHON

