// Trinity Voice Integration Test
// Copyright (c) Joshua
// Shared under license for Ask_Pete (Purdue University)

use trinity_body::voice_integration_manager::*;
use trinity_body::trinity_face::*;
use bevy::prelude::*;

#[tokio::test]
async fn test_voice_integration_complete_workflow() {
    // Initialize voice integration manager
    let mut manager = VoiceIntegrationManager::new().await.unwrap();
    
    // Start voice session
    let session_id = manager.start_voice_session(TrinityPersona::Sage);
    assert!(!session_id.is_empty());
    
    // Create test audio data (simulating microphone input)
    let test_audio = vec![0.1f32; 1600]; // 100ms at 16kHz
    
    // Process audio input
    let result = manager.process_audio_input(test_audio, TrinityPersona::Sage).await.unwrap();
    
    // Verify results
    assert!(!result.transcription.is_empty());
    assert!(result.confidence > 0.0);
    assert!(result.processing_time_ms > 0);
    assert!(result.educational_context.is_some());
    
    // Check educational context
    let context = result.educational_context.unwrap();
    assert!(matches!(context.domain, EducationalDomain::General));
    
    // Get statistics
    let stats = manager.get_voice_statistics();
    assert_eq!(stats.total_interactions, 1);
    assert_eq!(stats.successful_transcriptions, 1);
    assert!(stats.session_active);
    
    // End session
    let session = manager.end_voice_session().unwrap();
    assert_eq!(session.session_id, session_id);
    assert!(session.total_interactions >= 1);
    
    println!("✅ Voice integration workflow test passed!");
}

#[tokio::test]
async fn test_educational_context_extraction() {
    let manager = VoiceIntegrationManager::new().await.unwrap();
    
    // Test different educational domains
    let test_cases = vec![
        ("machine learning algorithms for educational software", EducationalDomain::ComputerScience),
        ("calculus fundamentals and mathematical concepts", EducationalDomain::Mathematics),
        ("photosynthesis and cellular biology processes", EducationalDomain::Science),
    ];
    
    for (transcription, expected_domain) in test_cases {
        let context = manager.extract_educational_context(transcription);
        assert!(context.is_some());
        
        let context = context.unwrap();
        assert_eq!(context.domain, expected_domain);
        assert!(!context.key_concepts.is_empty());
    }
    
    println!("✅ Educational context extraction test passed!");
}

#[test]
fn test_voice_processing_priorities() {
    let priorities = vec![
        ProcessingPriority::High,
        ProcessingPriority::Medium,
        ProcessingPriority::Low,
    ];
    
    // Verify priority ordering
    assert!(ProcessingPriority::High > ProcessingPriority::Medium);
    assert!(ProcessingPriority::Medium > ProcessingPriority::Low);
    
    println!("✅ Voice processing priorities test passed!");
}
