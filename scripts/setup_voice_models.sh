## Troubleshooting

### Voice Recognition Not Working
1. Check microphone permissions
2. Verify model files exist in `assets/models/voice/`
3. Check NPU driver installation
4. Review logs for error messages

### High Latency
1. Ensure NPU drivers are loaded
2. Check system memory usage
3. Verify audio sample rate (16kHz required)
4. Monitor CPU usage during processing

### Poor Accuracy
1. Check audio quality (clear speech required)
2. Verify language settings (English optimized)
3. Adjust microphone sensitivity
4. Check for background noise

